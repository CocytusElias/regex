// 座机
export const LandlinePhone = /^(\(\d{3,4}\)|\d{3,4}-|\s)?\d{7,14}$/

// 手机
export const mobilePhone = /^1(3\d|4[5-9]|5[0-35-9]|6[2567]|7[0-8]|8\d|9[0-35-9])\d{8}$/

// 身份证
export const identityCard = /(^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$)|(^[1-9]\d{5}\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}$)/

// 姓名，设定为2-10位主要考虑到少数民族
export const CnName = /^[\u4E00-\u9FA5]{2,10}$/

// 年月日时分秒 格式 2029-01-02 00:00:00
export const DateTime = /^((([0-9]{3}[1-9]|[0-9]{2}[1-9][0-9]|[0-9][1-9][0-9]{2}|[1-9][0-9]{3})-(((0[13578]|1[02])-(0[1-9]|[12][0-9]|3[01]))|((0[469]|11)-(0[1-9]|[12][0-9]|30))|(02-(0[1-9]|[1][0-9]|2[0-8]))))|((([0-9]{2})(0[48]|[2468][048]|[13579][26])|((0[48]|[2468][048]|[3579][26])00))-02-29))\s+([0-1]?[0-9]|2[0-3]):([0-5][0-9]):([0-5][0-9])$/

// 年月日 格式 2029-01-02
export const Date = /^(?:(?!0000)[0-9]{4}-(?:(?:0[1-9]|1[0-2])-(?:0[1-9]|1[0-9]|2[0-8])|(?:0[13-9]|1[0-2])-(?:29|30)|(?:0[13578]|1[02])-31)|(?:[0-9]{2}(?:0[48]|[2468][048]|[13579][26])|(?:0[48]|[2468][048]|[13579][26])00)-02-29)$/

// 月日验证 格式 01-02
export const MonthDay = /^(0[1-9]|1[0-2])-(0[1-9]|1[0-9]|2[0-9])$/

// 时分秒验证 格式00:00:00
export const Time = /^([0-1]?[0-9]|2[0-3]):([0-5][0-9]):([0-5][0-9])$/

// 时分验证 格式00:00 小时是1-23，分钟是0-59
export const HoursMinutes = /^([0-1]?[0-9]|2[0-3]):([0-5][0-9])$/

// 分秒验证 格式00:00 分钟和秒都是0-59
export const MinutesSeconds = /^([0-5][0-9]):([0-5][0-9])$/

// 网址
export const HttpUrl = /^(http:\/\/)[^\s]+/
export const HttpsUrl = /^(https:\/\/)[^\s]+/
export const WebUrl = /^((https|http)?:\/\/)[^\s]+/

// ip验证
export const Ipv4 = /^((2(5[0-5]|[0-4]\d))|[0-1]?\d{1,2})(\.((2(5[0-5]|[0-4]\d))|[0-1]?\d{1,2})){3}$/
